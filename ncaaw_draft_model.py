'''
Clustering Model using sklearn
'''

import numpy as np
import pandas as pd

from sklearn.impute import SimpleImputer
from sklearn.preprocessing import StandardScaler
from sklearn.cluster import KMeans
from sklearn.pipeline import Pipeline
from sklearn.metrics import silhouette_score
from sklearn.decomposition import PCA

import matplotlib.pyplot as plt
import files

# Load Data
PATH = "ncaaw_all_players_2025.csv"
df = pd.read_csv(PATH)
print("Rows, Cols:", df.shape)


features = [
    # Usage & Scoring Role
    "pts_per_g",
    "fga_per_g",
    "usg_pct",

    # Shooting Profile
    "ts_pct",
    "fg3_pct",
    "fg3a_per_g",
    "fta_per_g",

    # Playmaking
    "ast_per_g",
    "ast_pct",
    "tov_per_g",

    # Rebounding / Size Proxy
    "treb_per_g",
    "treb_pct",

    # Defense
    "dws",
    "stl_per_g",
    "blk_per_g",
]

missing = [c for c in features if c not in df.columns]
if missing:
    raise ValueError(f"Missing columns in CSV: {missing}\n"
                     f"Available columns example: {list(df.columns)[:30]}")

X = df[features].copy()

# Convert to numeric (coerce errors to NaN)
for c in features:
    X[c] = pd.to_numeric(X[c], errors="coerce")

# Optional: convert percent columns from 0–100 to 0–1 if needed
pct_cols = ["usg_pct", "ts_pct", "fg3_pct", "ast_pct", "trb_pct"]
for c in pct_cols:
    if c in X.columns:
        med = np.nanmedian(X[c].values)
        # heuristic: if median > 1.5, likely percent out of 100
        if med > 1.5:
            X[c] = X[c] / 100.0

# -------------------------
# 3) Choose k (silhouette sweep)
# -------------------------
k_values = list(range(4, 11))
sil_scores = []

for k in k_values:
    pipe = Pipeline(steps=[
        ("imputer", SimpleImputer(strategy="median")),
        ("scaler", StandardScaler()),
        ("kmeans", KMeans(n_clusters=k, random_state=42, n_init="auto")),
    ])
    labels = pipe.fit_predict(X)

    X_imp = pipe.named_steps["imputer"].transform(X)
    X_scaled = pipe.named_steps["scaler"].transform(X_imp)
    sil = silhouette_score(X_scaled, labels)
    sil_scores.append(sil)

plt.figure()
plt.plot(k_values, sil_scores, marker="o")
plt.xlabel("k (number of clusters)")
plt.ylabel("Silhouette score")
plt.title("Silhouette score by k")
plt.show()

best_k = k_values[int(np.argmax(sil_scores))]
print("Best k by silhouette:", best_k)

# Set K
K = best_k

# -------------------------
# 4) Fit final KMeans
# -------------------------
final_pipe = Pipeline(steps=[
    ("imputer", SimpleImputer(strategy="median")),
    ("scaler", StandardScaler()),
    ("kmeans", KMeans(n_clusters=K, random_state=42, n_init="auto")),
])

df["cluster"] = final_pipe.fit_predict(X)

# -------------------------
# 5) Visualization: PCA to 2D
# -------------------------
# PCA is for visualization only (clusters were fit in full feature space)
X_imp = final_pipe.named_steps["imputer"].transform(X)
X_scaled = final_pipe.named_steps["scaler"].transform(X_imp)

pca = PCA(n_components=2, random_state=42)
X_2d = pca.fit_transform(X_scaled)

df["pca1"] = X_2d[:, 0]
df["pca2"] = X_2d[:, 1]

print("PCA explained variance (2 components):", pca.explained_variance_ratio_.round(3),
      "Total:", pca.explained_variance_ratio_.sum().round(3))

plt.figure(figsize=(9, 6))
plt.scatter(df["pca1"], df["pca2"], c=df["cluster"], s=10)
plt.xlabel("PCA 1")
plt.ylabel("PCA 2")
plt.title("NCAAW Player Archetypes (KMeans clusters) — PCA visualization")
plt.show()

# -------------------------
# 6) Cluster profiles (means in original units, after imputation)
# -------------------------
X_imputed_df = pd.DataFrame(X_imp, columns=features, index=df.index)

cluster_profile = (
    pd.concat([df[["cluster"]], X_imputed_df], axis=1)
      .groupby("cluster")[features]
      .mean()
      .round(3)
)

print("\nCluster counts:")
print(df["cluster"].value_counts().sort_index())

print("\nCluster profiles (feature means):")
display(cluster_profile)

# -------------------------
# 7) Export
# -------------------------
OUT_PATH = "ncaaw_players_with_clusters_2025.csv"
df.to_csv(OUT_PATH, index=False)
print("Wrote:", OUT_PATH)

