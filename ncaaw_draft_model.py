# Women’s team pages on Sports-Reference typically use these patterns
TEAM_HREF_PATTERNS = [
    re.compile(r"^/cbb/schools/[^/]+/women/\d{4}\.html$"),
    re.compile(r"^/cbb/schools/[^/]+/women/?$"),
    re.compile(r"^/cbb/schools/[^/]+/women\.html$"),
]

def get_soup(url: str) -> BeautifulSoup:
    r = requests.get(url, headers=HEADERS, timeout=30)
    r.raise_for_status()
    return BeautifulSoup(r.text, "html.parser")

def soups_including_comments(soup: BeautifulSoup):
    """Sports-Reference sometimes hides tables inside HTML comments."""
    soups = [soup]
    for c in soup.find_all(string=lambda t: isinstance(t, Comment)):
        if "<table" in c:
            soups.append(BeautifulSoup(c, "html.parser"))
    return soups

def find_standings_table(soup: BeautifulSoup):
    """Find standings table (id='standings' or heuristic match)."""
    for s in soups_including_comments(soup):
        t = s.find("table", id="standings")
        if t is not None:
            return t

    # heuristic fallback: table with W, L, W-L%
    for s in soups_including_comments(soup):
        for table in s.find_all("table"):
            thead = table.find("thead")
            if not thead:
                continue
            headers = [th.get_text(strip=True) for th in thead.find_all("th")]
            hs = set(headers)
            if {"W", "L", "W-L%"}.issubset(hs) and ("School" in hs or "Team" in hs):
                return table
    return None

def get_cell_text(tr, data_stat: str) -> str | None:
    """Extract cell text using Sports-Reference's data-stat attribute."""
    td = tr.find(["td", "th"], attrs={"data-stat": data_stat})
    if td is None:
        return None
    return td.get_text(strip=True)

def extract_teams_wins_losses(table, conference_name: str, conference_slug: str, year: int):
    teams = []
    seen = set()

    tbody = table.find("tbody")
    if not tbody:
        return teams

    for tr in tbody.find_all("tr"):
        # Skip separator/header rows inside tbody
        if tr.get("class") and ("thead" in tr.get("class") or "spacer" in tr.get("class")):
            continue

        a = tr.find("a", href=True)
        if not a:
            continue

        href = a["href"].strip()
        if not any(p.match(href) for p in TEAM_HREF_PATTERNS):
            continue

        team_name = a.get_text(strip=True)
        team_url = urljoin(BASE, href)
        # /cbb/schools/<team_id>/...
        team_id = href.split("/")[3]

        # Wins / losses: usually data-stat="wins" and data-stat="losses"
        wins = get_cell_text(tr, "wins")
        losses = get_cell_text(tr, "losses")

        # Sometimes the columns can be named differently; fallback to visible W/L columns:
        if wins is None or losses is None:
            # try common alternates
            wins = wins or get_cell_text(tr, "w")
            losses = losses or get_cell_text(tr, "l")

        # W-L% if you want it
        wl_pct = get_cell_text(tr, "win_loss_pct") or get_cell_text(tr, "win_loss_perc")

        if wins is None or losses is None:
            # As a last resort, attempt to locate the W/L by header text positions
            # (rarely needed, but keeps the scraper resilient)
            tds = [c.get_text(strip=True) for c in tr.find_all(["td", "th"])]
            # Heuristic: find first integer-like pair
            nums = [x for x in tds if re.fullmatch(r"-?\d+", x)]
            # not guaranteed; if missing, leave blank
            if wins is None and len(nums) >= 2:
                wins = nums[0]
            if losses is None and len(nums) >= 2:
                losses = nums[1]

        key = (conference_slug, year, team_id)
        if key in seen:
            continue
        seen.add(key)

        teams.append({
            "season_year": year,
            "conference": conference_name,
            "conference_slug": conference_slug,
            "team": team_name,
            "team_id": team_id,
            "wins": int(wins) if wins and wins.isdigit() else wins,
            "losses": int(losses) if losses and losses.isdigit() else losses,
            "wl_pct": float(wl_pct) if wl_pct and re.fullmatch(r"\d*\.\d+", wl_pct) else wl_pct,
            "team_url": team_url,
        })

    return teams

def scrape_conference(conference_name: str, conference_slug: str, year: int):
    url = f"{BASE}/cbb/conferences/{conference_slug}/women/{year}.html"
    soup = get_soup(url)
    table = find_standings_table(soup)
    if table is None:
        raise RuntimeError(f"Could not find standings table at {url}")

    rows = extract_teams_wins_losses(table, conference_name, conference_slug, year)
    if not rows:
        raise RuntimeError(f"Found standings table but extracted 0 teams at {url}")
    return rows

def scrape_all_conferences(conferences, year: int, sleep_s: float = 1.0) -> pd.DataFrame:
    all_rows = []
    for conf_name, conf_slug in conferences:
        try:
            rows = scrape_conference(conf_name, conf_slug, year)
            all_rows.extend(rows)
            print(f"[OK] {conf_name}: {len(rows)} teams")
        except Exception as e:
            print(f"[FAIL] {conf_name} ({conf_slug}): {e}")
        time.sleep(sleep_s)

    df = pd.DataFrame(all_rows)

    # De-dupe (shouldn’t be necessary, but safe)
    df = df.drop_duplicates(subset=["season_year", "team_id"]).sort_values(["conference", "team"]).reset_index(drop=True)
    return df

